type TranslationKey = keyof (typeof STRINGS)["en"];

export function tr(locale: Locale, key: TranslationKey, params?: Record<string, string | number>): string {
  let text: string = STRINGS[locale][key] ?? STRINGS.en[key];
  if (!params) {
    return text;
  }

  for (const [k, v] of Object.entries(params)) {
    text = text.replaceAll(`{${k}}`, String(v));
  }
  return text;
}
